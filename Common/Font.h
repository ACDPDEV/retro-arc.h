array<string, 9> ConcatFont(vector<array<string, 9>> FONTs, int separator) {
    array<string, 9> result { "", "", "", "", "", "", "", "", "" };
    string sep = "";
    for (int i = 0; i < separator; i++) {
        sep += " ";
    }
    for (int i = 0; i < 9; i++) {
        for (int j = 0; j < FONTs.size(); j++) {
            result[i] += FONTs[j][i] + sep;
        }
    }
    return result;
}
